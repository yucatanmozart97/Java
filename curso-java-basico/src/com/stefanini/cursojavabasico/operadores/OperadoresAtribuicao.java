package com.stefanini.cursojavabasico.operadores;

public class OperadoresAtribuicao {

	public static void main(String[] args) {
		int a = 10; // operador de atribuição
		int b = 5;
		
		b += a; // aditivo
		b -= a; // subtrativo
		b *= a; // multiplicativo
		b /= a; // diviso
		b %= a; // modular
		
	}
}
